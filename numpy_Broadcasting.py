# Importing the NumPy library for numerical operations
import numpy as np  

# Creating a NumPy array with elements 1, 2, and 3
a = np.array([1, 2, 3])  

# Defining a scalar value to multiply with the array
b = 2  

# Performing element-wise multiplication of array 'a' with scalar 'b'
# Each element in 'a' is multiplied by 2
print(a * b)  # Output: [2 4 6]
